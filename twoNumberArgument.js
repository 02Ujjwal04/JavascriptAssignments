function calculateSum(num1, num2) {
    // Variable declaration
    var result;
  
    // Log variable value before assignment
    console.log(result); // Output: undefined
  
    // Calculate sum
    result = num1 + num2;
  
    return result;
  }
  
  // Call the function
  console.log(calculateSum(2, 3)); // Output: 5
  